package utils;

public class BinaryCalculator {
	public int sum(int d1, int d2){
		return d1+d2;
	}
	
	public int sub(int d1, int d2){
		return d1-d2;
	}
	
	public int mult(int d1, int d2){
		return d1*d2;
	}
	
	public int div(int d1, int d2){
		return d1/d2;
	}
	
	public void infiniteLoop() {
		while(true);
	}
}
